#include <string>

static void char2hex(char dec, char *outStr) {
	char dig1 = (dec&0xF0)>>4;
	char dig2 = (dec&0x0F);
	if ( 0<= dig1 && dig1<= 9) dig1+=48;    //0,48 in ascii
	if (10<= dig1 && dig1<=15) dig1+=65-10; //A,65 in ascii
	if ( 0<= dig2 && dig2<= 9) dig2+=48;
	if (10<= dig2 && dig2<=15) dig2+=65-10;

	outStr[0] = dig1;
    outStr[1] = dig2;
}

int str2netstr(const char *inStr, char *outStr) {
    const char *c = inStr;
    int len = 0;
	while (*c) {
		if ( (48 <= c[0] && c[0] <= 57) ||//0-9
			(65 <= c[0] && c[0] <= 90) ||//ABC...XYZ
			(97 <= c[0] && c[0] <= 122) || //abc...xyz
			(c[0]=='~' || c[0]=='-' || c[0]=='_' || c[0]=='.')
			) {
            outStr[0] = c[0];
            outStr++;
            c++;
            len++;
		} else {
            outStr[0] = '%';
            char2hex(c[0], &outStr[1]);
            c++;
            outStr += 3;
            len += 3;
		}
	}
    outStr[len] = 0;
	return len;
}

